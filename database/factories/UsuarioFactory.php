class UsuarioFactory extends Factory
{
    /**
     * Define the model's default state.
     *
     * @return array<string, mixed>
     */
    public function definition()
    {
        
        return [
            'nombre' => fake()->name(),
            'empresa' => fake()->company(),
            'nif_cif' => fake()->word(),
